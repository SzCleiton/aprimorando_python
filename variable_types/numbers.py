# Int
number_int = 10
print("Integer:", number_int)

# Real with floating point
number_real = 10.5
print("Real:", number_real)

# type()
print("Type of var int:", type(number_int))

#some +
some = 1 + 1
print("1 + 1 :", some)

#sub -
sub = 10 - 5
print("10 - 5 :", sub)

# multiplication *
mult = 2 * 3
print("2 * 3 :", mult)

# div /
div = 5 / 2
print("5 / 2 :", div)
print("Type of var div:", type(div))

# int()
print("Value in int: ", int(div))  # Convert to int

# float()
print("Value in float: ", float(some))  # Convert to float

# module %
mod = 5 % 2
print("5 % 2 :", mod)

# div //
div = 5 // 2
print("5 // 2 :", div)
print("Type of var div:", type(div))